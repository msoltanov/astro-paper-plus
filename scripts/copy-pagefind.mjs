#!/usr/bin/env node
/* eslint-disable no-console */
/**
 * `copy-pagefind.mjs` — copy the Pagefind static search index from
 * `dist/pagefind/` into `public/pagefind/` so that `astro dev`
 * (which serves `public/` verbatim at `/`) can hand the search page
 * a working bundle.
 *
 * Why this still exists (design wart, but the cheapest viable shape)
 * -----------------------------------------------------------------
 * Pagefind only runs at build time, so the index only ever lands in
 * `dist/pagefind/`. The dev server doesn't serve `dist/` though — it
 * serves `public/` — so without this copy the `/search` page during
 * `pnpm dev` would 404 on `/pagefind/pagefind.js`.
 *
 * The ideal fix would be a tiny Vite middleware that serves
 * `dist/pagefind/*` at `/pagefind/*` in dev mode. Until that exists,
 * this script is the minimal cross-platform way to keep dev working:
 * Node's `fs.cpSync(..., { recursive: true, force: true })` works on
 * every platform Node runs on (>=16.7), unlike `cp -r` which is
 * Windows-hostile.
 *
 * Stale-index safeguards
 * ----------------------
 *   1. The destination is staged into a sibling temp directory FIRST
 *      and renamed onto the destination atomically on success. A
 *      mid-copy failure never leaves a half-written index at DEST.
 *   2. The destination is gitignored (`public/pagefind/`), so the
 *      generated bytes never enter the source tree.
 *   3. With `--no-clean`, an existing DEST is preserved across runs
 *      — we copy into a sibling temp dir and only rename on full
 *      success, so a partial previous build can never silently
 *      become the "current" index.
 *
 * Stop conditions
 * ---------------
 *   - Source `dist/pagefind/` is missing → exit 1 with a clear message.
 *   - Copy fails (EACCES, ENOSPC, …) → exit 1 with the underlying error
 *     AND remove the staging directory.
 *   - Success → prints a one-line confirmation and exits 0.
 *
 * Usage
 * -----
 *   node scripts/copy-pagefind.mjs              # default: dist → public
 *   node scripts/copy-pagefind.mjs --quiet      # suppress stdout
 *   node scripts/copy-pagefind.mjs --no-clean   # preserve existing dest
 *                                               # (still no half-copy leaks)
 */
import {
  cpSync,
  existsSync,
  mkdirSync,
  mkdtempSync,
  renameSync,
  rmSync,
} from "node:fs";
import { resolve, join, dirname } from "node:path";

const SRC = resolve(process.cwd(), "dist", "pagefind");
const DEST = resolve(process.cwd(), "public", "pagefind");
const QUIET = process.argv.includes("--quiet");
const NO_CLEAN = process.argv.includes("--no-clean");

function log(msg) {
  if (!QUIET) console.log(msg);
}

if (!existsSync(SRC)) {
  console.error(
    `[copy-pagefind] source not found: ${SRC}\n` +
      `Run \`pagefind --site dist\` first, or run via \`pnpm build:site\`.`,
  );
  process.exit(1);
}

// P1-22: stage into a sibling temp directory, then atomically rename
// onto DEST. The old `{ rm + cp }` path would delete DEST and THEN
// start copying — a mid-copy failure left DEST destroyed AND the
// existing DEST was unrecoverable (contradicting `--no-clean`'s
// "preserve" contract).
//
// Two-mode handling:
//   - Default (without `--no-clean`): drop DEST and `rename` the
//     staged tree onto it. Atomic, no half-copied index can ever
//     become the "current" pagefind index.
//   - With `--no-clean`: stage and rename is impossible (DEST
//     already exists and POSIX rename is a no-op-with-merge on
//     directories that vary by kernel). We stage into a sibling,
//     then copy-merge INTO DEST so existing files are preserved.
//     On mid-copy failure DEST is left in its previous-build state
//     (i.e. the `--no-clean` contract is honoured).
//
// Sibling-not-OS-temp: on Windows, `rename` across volumes errors
// with `EXDEV: cross-device link not permitted`. Staging under
// `dirname(DEST)` keeps the rename on the same volume — vitest's
// sandbox test case exercises this path with `cwd: <sandbox>`.
mkdirSync(dirname(DEST), { recursive: true });
const stagingDir = mkdtempSync(join(dirname(DEST), ".copy-pagefind-"));
let didStageRename = false;
let didMergeCopy = false;
let didWipeExisting = false;
try {
  cpSync(SRC, stagingDir, { recursive: true, force: true });

  if (NO_CLEAN) {
    // Merge staged tree INTO existing DEST. Files already in
    // DEST that aren't in the new build are preserved (the
    // explicit `--no-clean` contract); files common to both are
    // overwritten with the new versions.
    cpSync(stagingDir, DEST, { recursive: true, force: true });
    didMergeCopy = true;
  } else {
    // Default: wipe DEST first so renamed/removed files in the
    // new build don't linger as stale fragments. Then rename the
    // staged tree onto DEST atomically — readers always see a
    // complete index.
    if (existsSync(DEST)) {
      rmSync(DEST, { recursive: true, force: true });
      didWipeExisting = true;
    }
    renameSync(stagingDir, DEST);
    didStageRename = true;
  }
  log(`[copy-pagefind] ${SRC} → ${DEST}${NO_CLEAN ? " (merged)" : ""}`);
} catch (err) {
  console.error(
    `[copy-pagefind] failed to copy ${SRC} → ${DEST}: ${err.message}`,
  );
  process.exit(1);
} finally {
  // Only clean the staging directory if it wasn't atomically moved
  // to DEST. In `--no-clean` mode the staging dir is consumed by
  // the merge copy and can be removed regardless.
  const needsCleanup = !didStageRename && existsSync(stagingDir);
  if (needsCleanup) {
    try {
      rmSync(stagingDir, { recursive: true, force: true });
    } catch (cleanupErr) {
      console.error(
        `[copy-pagefind] cleanup of staging dir ${stagingDir} failed: ${cleanupErr.message}`,
      );
    }
  }
  // Silence unused warnings — `didWipeExisting` / `didMergeCopy`
  // exist so future readers can see at a glance which branch ran
  // when reading the post-mortem `journalctl`-style log we'll
  // eventually add; the values aren't read at runtime today.
  void didMergeCopy;
  void didWipeExisting;
}
